document.getElementById("OptionUp1").addEventListener("mouseover", mouseOverUp1);
document.getElementById("OptionUp1").addEventListener("mouseout", mouseOutUp1);

function mouseOverUp1() {
  document.getElementById("OptionUp1").style.backgroundColor = "#23AD93"; // vert
}

function mouseOutUp1() {
  document.getElementById("OptionUp1").style.backgroundColor = "#EE592B"; // orange
}


